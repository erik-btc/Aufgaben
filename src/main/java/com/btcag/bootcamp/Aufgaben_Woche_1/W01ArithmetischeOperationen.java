package com.btcag.bootcamp.Aufgaben_Woche_1;

public class W01ArithmetischeOperationen {
    public static void main(String[] args) {
        long var = 5;
        System.out.println(++var); // increment bevor ausführung
        System.out.println(var++); // increment nach ausführung
        System.out.println(var); // kein increment
        }
    }

